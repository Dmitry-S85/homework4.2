my_string = 'Создана строка, для выполнения домашнего задания'

#Вывод строки my_string в верхнем регистре.
print(my_string.upper())

#Вывод строк my_string в нижнем регистре.
print(my_string.lower())

#Изменение строки my_string, удаление всех пробелов.
print(my_string.replace(' ', ''))

#Вывод первого символа строки my_string.
print(my_string[0])

#Вывод последнего символа строки my_string.
print(my_string[-1])